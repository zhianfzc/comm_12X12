{
	ISP_U16 u16Num,u16i;
	u16Num = sizeof(gau16SnsInit_sc401AI)/sizeof(gau16SnsInit_sc401AI[0]);
	for(u16i=0; u16i < u16Num; u16i++)
	{
		sensor_write_register(IspDev,(ISP_U32)gau16SnsInit_sc401AI[u16i][0], (ISP_U32)gau16SnsInit_sc401AI[u16i][1]);
	}
	XM_MPI_ISP_USleep(20000);
	DEBUG("------------- sc401AI 4M init ok! (@2020202_126) ----------------\n");
}



