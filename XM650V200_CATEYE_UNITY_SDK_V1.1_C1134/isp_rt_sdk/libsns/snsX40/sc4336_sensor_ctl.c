{
	ISP_U16 u16Num,u16i;
	u16Num = sizeof(gau16SnsInit_sc4336)/sizeof(gau16SnsInit_sc4336[0]);
	for(u16i=0; u16i < u16Num; u16i++)
	{
		sensor_write_register(IspDev,(ISP_U32)gau16SnsInit_sc4336[u16i][0], (ISP_U32)gau16SnsInit_sc4336[u16i][1]);
	}
	XM_MPI_ISP_USleep(20000);
	DEBUG("------------- sc4336 4M init ok! ----------------\n");
}



